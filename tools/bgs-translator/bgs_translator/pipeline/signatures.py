def signature_explanation(signature: str) -> str:
    """Return a concise explanation for one four-character record signature."""

    normalized = str(signature or "").upper()
    return SIGNATURE_EXPLANATIONS.get(normalized, "未知或少见 record 类型；翻译时请主要依据 EDID、字段和原文语境。")


def render_signature_context(signatures: Iterable[str]) -> str:
    """Render only the explanations needed by the current batch."""

    normalized = sorted({str(signature or "").upper() for signature in signatures if str(signature or "").strip()})
    if not normalized:
        return ""
    return "\n".join(f"- {signature}: {signature_explanation(signature)}" for signature in normalized)


